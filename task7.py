#todo: Даны три точки A , B , C на числовой оси. Найти длины отрезков AC и BC и их сумму.
# Примечание: все точки получаем через функцию input().

a = int(input("Введите знаачение для точки А: "))
b = int(input("Введите знаачение для точки B: "))
c = int(input("Введите знаачение для точки C: "))
print(f"Длина отрезка AB: {a*c}, Длина отрезка BC: {b*c}, Сумма отрезков: {a*c+b*c} ")
